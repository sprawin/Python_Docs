print("nums[3:10] ",nums[3:10])
print("nums[1:]: ",nums[1:])
print("nums[:4]: ",nums[:4])
print("nums[-1:]: ",nums[-1:])
print("nums[-2:]: ",nums[-2:])
print("nums[:-4]: ",nums[:-4])
print("nums[:-3]: ",nums[:-3])

