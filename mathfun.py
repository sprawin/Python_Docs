# Math Functions

import math


x = 25
print("math.sqrt(x) ",math.sqrt(x)) 
print("math.pow(3,2) ",math.pow(3,2))
print("math.pi ",math.pi)
print("math.e ",math.e)

num = [2.5, 2.1, 2.9]

print("\nCeil and Floor Functions \n")
print(num,"\n")
for n in num:
    print("math.ceil(n)",math.ceil(n))
    print("math.floor(n)", math.floor(n))
